#include <iostream>
using namespace std;


//Это учебная программа
int main(void)
{
    int a, b;
    cout << "Enter two numbers: ";
    cin >> a >> b;
    cout << a << " + " << b << " = " << a + b << endl;
    cout << a << " + " << b << " = " << a - b << endl;
    cout << a << " * " << b << " = " << a * b << endl;
    return 0;
}